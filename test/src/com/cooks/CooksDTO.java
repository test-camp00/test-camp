package com.cooks;

public class CooksDTO {
	private int num, listNum, wantedCount;
	private String cookCode, cookName, cookplay;
	private String name, ways, memo, fileName;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getListNum() {
		return listNum;
	}
	public void setListNum(int listNum) {
		this.listNum = listNum;
	}
	public String getCookCode() {
		return cookCode;
	}
	public void setCookCode(String cookCode) {
		this.cookCode = cookCode;
	}
	public String getCookName() {
		return cookName;
	}
	public void setCookName(String cookName) {
		this.cookName = cookName;
	}	
	
	public String getCookplay() {
		return cookplay;
	}
	public void setCookplay(String cookplay) {
		this.cookplay = cookplay;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWays() {
		return ways;
	}
	public void setWays(String ways) {
		this.ways = ways;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getWantedCount() {
		return wantedCount;
	}
	public void setWantedCount(int wantedCount) {
		this.wantedCount = wantedCount;
	}
	
	

}
